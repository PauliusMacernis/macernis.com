<p>Tautos poreikio gyventi gerai vakuumą užpildo nauji nariai, gali kilti sumaištis, pvz. pilietinis karas dėl neužimto sosto ar pan. Vakuuma užpildymas turi būti efektyvus - pvz. "vakuumo taškas > autoritetas" aptikimais ir išrinkimais</p>
<p>Problema yra valdininkų patikimumas. Jeigu valdininkai dabartinėje valdžioje yra maksimaliai kompetetingi - visuomenėje vakuumo nėra. Jeigu valdininkai stinga kompetencijos - susikuria vakuumas ir jo vietą siekia /„turėtų siekti) užpildyti kompetetingi asmenys. Pagrindinė problema rinkimuose - kaip išrinkti labiausiai kompetetingą asmenį.
